    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
    Connection conn = null;
    PreparedStatement pstmt = null;
    String JDBC_DRIVER = "com.mysql.cj.jdbc.Driver";
    String url = "jdbc:mysql://localhost:3306/Hospitaldb";
    String user = "root";
    String password = "Mysql@123";

    String PatientID = jTextField1.getText();
    try{
        Class.forName(JDBC_DRIVER);
        conn = DriverManager.getConnection(url, user, password);
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("Select Doctors.Name, Patients.Name, AppointmentDate, AppointmentTime, Diagnosis, MedicinesPrescribed, Tests, PastSurgeries, CurrCondition, Genetics from Diagnosis join Patients on Patients.PatientID=Diagnosis.PatientID join Doctors on Doctors.DocID=Diagnosis.DocID where Diagnosis.PatientID='" + PatientID + "'");

        // Populate jTable2 manually
        DefaultTableModel model = new DefaultTableModel();
        jTable2.setModel(model);

        model.addColumn("Doctor Name");
        model.addColumn("Patient Name");
        model.addColumn("Appointment Date");
        model.addColumn("Appointment Time");
        model.addColumn("Diagnosed As");
        model.addColumn("Medicines Prescribed");
        model.addColumn("Tests");
        model.addColumn("Past Surgeries");
        model.addColumn("Current Condition");
        model.addColumn("Genetics");

        while(rs.next()){
            Object[] row = new Object[10];
            for(int i = 0; i < 10; i++){
                row[i] = rs.getObject(i + 1);
            }
            model.addRow(row);
        }

        if (!rs.first()) {
            jLabel2.setVisible(true);
        } else {
            jLabel2.setVisible(false);
            jTextField1.setEditable(false);
        }

    } catch(Exception e) {
        JOptionPane.showMessageDialog(null, "Connection Error");
    }            
    }         
    
